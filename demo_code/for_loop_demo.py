def main():
    #print integers from 0 and 10
    for number in range(11):
        print(number)
    
    #print integers from 5 and 10
    print("\n\n")
    for number in range(5, 11):
        print(number)

    #print even integers from 0 and 10
    print("\n\n")
    for number in range(1, 11, 2):
        print(number)
    
    

main()